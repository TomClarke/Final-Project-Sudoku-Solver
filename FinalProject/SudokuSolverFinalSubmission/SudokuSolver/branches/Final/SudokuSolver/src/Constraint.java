package src;

import java.util.List;


/**
 * The Interface Constraint. that handles all of the constraints within the network
 */
public interface Constraint {

	/**
	 * Constrains the values that the variables can take on.
	 *
	 * @param variableList the list of variables
	 * @return the list of a solved board.
	 */

	public abstract List<Variable> applyConstraints(List<Variable> variableList);

	/**
	 * Solved.
	 *
	 * @return true, if successful
	 */
	public abstract boolean solved();


}
